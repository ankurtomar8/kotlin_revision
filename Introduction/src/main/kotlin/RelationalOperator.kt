package org.example

fun main(){

    // Relation Operator
    var ankurMarks = 79
    var ankitMarks = 90

    println(ankitMarks > ankurMarks) //true
    println(ankurMarks < ankitMarks ) // true
    println(ankurMarks ==  ankitMarks) // false
    println(ankitMarks >= ankurMarks) // true
    println(ankitMarks <= ankurMarks) // false
    println(ankitMarks != ankurMarks) // true





}